num_1 = input('Enter first number: ')
num_2 = input('Enter second number: ')

print(f'Value of number before swapping: {num_1}')
print(f'Value of number before swapping: {num_2}')

temp = num_1
num_1 = num_2
num_2 = temp

print('-------------------------------------------------')
print(f'Value of number after swapping: {num_1}')
print(f'Value of number after swapping: {num_2}')
